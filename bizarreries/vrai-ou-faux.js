// Boolean
if (true) {
  ('Hello Dev Day!');
}

// String
const event = 'Dev Day';
if (event) {
  'Hello ' + event + '!';
}

// Number
const year = 2018;
if (year) {
  'Hello ' + event + ' ' + year + '!';
}

// Object
const now = new Date();
if (now) {
  '[' + now + '] Hello ' + event + ' ' + year + '!';
}

// Obviously!
3 > 2 > 1;
